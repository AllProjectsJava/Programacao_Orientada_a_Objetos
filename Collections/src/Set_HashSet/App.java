package Set_HashSet;

import java.util.Set;
import java.util.HashSet;

public class App {
	public static void main(String[] args) {
		System.out.println("HashSet");
		Set<String> hashSet = new HashSet<>();
		hashSet.add("Dd");
		hashSet.add("da");
		hashSet.add("B");
		hashSet.add("A");
		System.out.println(hashSet);	
	}
}
